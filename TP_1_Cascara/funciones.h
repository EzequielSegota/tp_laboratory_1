/** \brief suma dos numeros decimales
 *
 * \param A float (primer operandado)
 * \param B float (segundo operando)
 * \return float  (resultado de suma)
 *
 */
float suma(float A, float B);

/** \brief resta dos numeros decimales
 *
 * \param A float (primer operando)
 * \param B float (segundo operando)
 * \return float  (resultado de resta)
 *
 */
float resta(float A, float B);

/** \brief division de dos numeros decimales
 *
 * \param A float   dividendo
 * \param B float   divisor
 * \return float    resultado
 *
 */
float division(float A, float B);


/** \brief multiplicación de dos numeros decimales
 *
 * \param A float   multiplicando
 * \param B float   multiplicador
 * \return float    producto
 *
 */
float multiplicacion(float A, float B);

/** \brief El factoreo de el numero ingresado
 *
 * \param A float numero a ser factoreado
 * \return float resultado de factoreo
 *
 */
float factorial(float A);

/** \brief Toma los valores de A y B, realiza todas las operaciones, las muestra en un printf
 *
 * \param A int
 * \param B int
 * \return void
 *
 *
 */
void todasLasOperaciones(float A,float B);
